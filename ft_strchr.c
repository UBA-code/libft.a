#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int				i;
	char			*str;

	str = (char *) s;
	i = 0;
	while (str[i])
	{
		if (s[i] == (char)c)
			return ((char *)str + i);
		i++;
	}
	if (str[i] == (char)c)
		return ((char *)str + i);
	return (0);
}
